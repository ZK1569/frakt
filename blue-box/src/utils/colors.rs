pub fn color_palette(t: f32) -> (u8, u8, u8) {
    let a = (0.500, 0.500, 0.500);
    let b = (0.500, 0.500, 0.500);
    let c = (1.000, 1.000, 1.000);
    let d = (0.000, 0.333, 0.667);
    // let a = (1.000, 0.500, 0.500);
    // let b = (0.500, 0.500, 0.500);
    // let c = (0.750, 1.000, 0.667);
    // let d = (0.800, 1.000, 0.333);

    let r = b.0 * (6.28318 * (c.0 * t + d.0)).cos() + a.0;
    let g = b.1 * (6.28318 * (c.1 * t + d.1)).cos() + a.1;
    let b = b.2 * (6.28318 * (c.2 * t + d.2)).cos() + a.2;

    ((255.0 * r) as u8, (255.0 * g) as u8, (255.0 * b) as u8)
}
